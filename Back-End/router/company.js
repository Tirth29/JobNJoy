import { CompanyUpdate, getCompany, newCompany } from '../controllers/company.js';

import express from 'express';
import { singleUpload } from '../middleware/multer.js';
import { isAuthenticated } from '../middleware/auth.js';
import { isAdmin } from '../middleware/auth.js';
const router = express.Router();

router.post('/newcompany', isAuthenticated,isAdmin, singleUpload, newCompany);
router.post('/updatecompany', isAuthenticated,isAdmin, singleUpload, CompanyUpdate);
router.get('/getcompany', isAuthenticated, getCompany);

export default router;